import Foundation

func variablesAndValues()
{
    // Variable
    var greeting: String = "Hello World"
    greeting += " of Swift"
    
    println(greeting)
    
    // Value
    let greeting_val = "Hello World"
    println(greeting_val)
    
    // Types
    var version: Double = 1.0
    let introduced: Int = 2014
    let isAwesome: Bool = true

}

